variable1="Este es un "+"codigo de como concatenar"

# Imprimir el resultado de la concatenación
print(variable1)

# Definir dos variables con cadenas de texto
variable2="Hola mundo"
variable3=" este es un codigo en python"

# Concatenar las dos variables en una tercera variable
variable4=variable2+variable3

# Imprimir el resultado de la concatenación
print(variable4)

# Concatenar una variable con un string directamente dentro de un print()
print(variable2 + " y " + variable3)

