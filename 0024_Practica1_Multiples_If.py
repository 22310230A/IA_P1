# Solicitar un número entre 1 y 4 al usuario
entrada = int(input('Introduce un número del 1 al 4:\n'))

# Verificar la opción seleccionada y mostrar el mensaje correspondiente
if entrada == 1:  # Si el número ingresado es 1
    print('Has seleccionado la opción número 1.')
if entrada == 2:  # Si el número ingresado es 2
    print('Has seleccionado la opción número 2.')
if entrada == 3:  # Si el número ingresado es 3
    print('Has seleccionado la opción número 3.')
if entrada == 4:  # Si el número ingresado es 4
    print('Has seleccionado la opción número 4.')
