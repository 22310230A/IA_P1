
# Definir una tupla con colores
tupla=("cafe","negro","rosa","morado")
# Solicitar al usuario que ingrese un color
colores=input("Ingresa un color:")
# Verificar si el color ingresado está en la tupla
if colores in tupla:
    print("El color esta en la tupla")# Se ejecuta si el color ingresado está en la tupla
else :
    print("El color no esta en la tupla")# Se ejecuta si el color ingresado no está en la tupla