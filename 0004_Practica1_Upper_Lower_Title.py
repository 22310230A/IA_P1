# Definir una variable con el nombre completo
nombre= "josue alejandro gomez soltero"
# Aplicar el método title() para poner la primera letra de cada palabra en mayúscula
nombre = nombre.title()

# Imprimir el nombre con el formato cambiado
print(nombre)
# Definir una variable con una frase
mensaje1="Esta Es Una Frase Para Ser Formateada."
# Convertir toda la frase a minúsculas usando lower()
mensaje1=mensaje1.lower()
# Imprimir la frase en minúsculas
print(mensaje1)

# Definir otra variable con una frase
mensaje2="Esta Es Una Frase Para Ser Formateada."

# Convertir la frase a mayúsculas usando upper()
mensaje2=mensaje2.upper()
# Imprimir la frase en mayúsculas
print(mensaje2)