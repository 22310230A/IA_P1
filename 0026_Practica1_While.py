# Inicializar la variable x
x = 0
# Primer bucle while: incrementa x en 5 hasta que x sea mayor que 15
while x <= 15:
	print(x)# Imprime el valor de x
	x += 5 # Incrementa x en 5 en cada iteración


# Segundo bucle while: decrementa x en 20 hasta que x sea menor que -100
while x >= -100:
	print(x) # Imprime el valor de x
	x -= 20 # Decrementa x en 20 en cada iteración

# Tercer bucle while: imprime el valor de x y lo decrementa en 1 hasta llegar a 0	
x=10
while x >= 0:
	print('El valor del bucle es:',x)# Imprime el valor de x
	x -= 1  # Decrementa x en 1 en cada iteración
